// provides something like a for loop
function loop(value, test, update, body) {
  // for (let i = 0; i < array.length; i++)
  for (let i = value; test(i); i = update(i)){
    body(i);
  }
}

// /////////////////////////////////////////////////////////////////////////////
// every ///////////////////////////////////////////////////////////////////////
// /////////////////////////////////////////////////////////////////////////////

function every(array, func) {
  for (let i = 0; i < array.length; i++){
    if (!func(array[i])){
      return false;
    }
  }
  return true;
}

